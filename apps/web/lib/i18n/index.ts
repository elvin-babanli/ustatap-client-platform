const dictionaries: Record<Locale, Dict> = {
  en: dictEn,
  az: dictAz,
  ru: dictRu,
};

export function getDict(locale: Locale): Dict {
  return dictionaries[locale] ?? dictEn;
}

/** Get localized category/service name by locale key */
export function getName(
  item: { nameEn?: string; nameAz?: string; nameRu?: string } | null,
  locale: Locale
): string {
  if (!item) return "";
  if (locale === "az" && item.nameAz) return item.nameAz;
  if (locale === "ru" && item.nameRu) return item.nameRu;
  return item.nameEn ?? item.nameAz ?? item.nameRu ?? "";
}

